const translations: Record<LanguageCode, Partial<Record<TranslationKey, string>>> = { en, hy, ru };

function formatTemplate(template: string, params?: TranslationParams) {
  if (!params) {
    return template;
  }

  return template.replace(/\{(\w+)\}/g, (_match, key: string) =>
    Object.prototype.hasOwnProperty.call(params, key) ? String(params[key]) : "",
  );
}

function isLanguageCode(value: string | null): value is LanguageCode {
  return value === "en" || value === "hy" || value === "ru";
}

function readStoredLanguage(): LanguageCode {
  if (typeof window === "undefined") {
    return "en";
  }

  const storedLanguage = window.localStorage.getItem(LANGUAGE_STORAGE_KEY);
  return isLanguageCode(storedLanguage) ? storedLanguage : "en";
}

export function setStoredLanguage(language: LanguageCode) {
  window.localStorage.setItem(LANGUAGE_STORAGE_KEY, language);
  window.dispatchEvent(new CustomEvent(LANGUAGE_CHANGED_EVENT, { detail: language }));
}

function subscribeToLanguageChanges(callback: () => void) {
  window.addEventListener("storage", callback);
  window.addEventListener(LANGUAGE_CHANGED_EVENT, callback);

  return () => {
    window.removeEventListener("storage", callback);
    window.removeEventListener(LANGUAGE_CHANGED_EVENT, callback);
  };
}

export function useLanguage() {
  const language = useSyncExternalStore<LanguageCode>(
    subscribeToLanguageChanges,
    readStoredLanguage,
    () => "en",
  );

  const changeLanguage = (nextLanguage: LanguageCode) => {
    setStoredLanguage(nextLanguage);
  };

  const t = (key: TranslationKey, params?: TranslationParams) =>
    formatTemplate(translations[language][key] ?? translations.en[key] ?? key, params);

  return { language, setLanguage: changeLanguage, t };
}

export function getStaticTranslation(
  language: LanguageCode,
  key: TranslationKey,
  params?: TranslationParams,
) {
  return formatTemplate(translations[language][key] ?? translations.en[key] ?? key, params);
}

export function translateRoomLabel(roomId: string, translate: (key: TranslationKey, params?: TranslationParams) => string) {
  if (roomId === "bathroom") return translate("bathroom");
  if (roomId === "kitchen") return translate("kitchen");
  if (roomId === "living-room") return translate("livingRoom");
  if (roomId === "bedroom") return translate("bedroom");
  if (roomId === "empty-room") return translate("emptyRoom");
  return roomId;
}

